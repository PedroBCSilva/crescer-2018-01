package aula;

class Calculadora {

    int somar(int x,int y){
        int soma=x+y;
        return soma;
    }

    int subtrair(int x,int y){
        int sub=x-y;
        return sub;
    }
    double multiplicar(int x,int y){
        double mult=x*y;
        return mult;
    }
    double dividir(int x,int y){
        double div=x/y;
        return div;
    }
}
